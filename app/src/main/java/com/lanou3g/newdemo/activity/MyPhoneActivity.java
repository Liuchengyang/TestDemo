public class MyPhoneActivity extends Activity implements View.OnClickListener {
    private TextView tv_tell_custom, tv_cancel;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_my_phone);
        tv_tell_custom = (TextView) findViewById(R.id.tv_tell_custom);
        tv_cancel = (TextView) findViewById(R.id.tv_cancel);

        tv_tell_custom.setOnClickListener(this);
        tv_cancel.setOnClickListener(this);
        getWindow().setLayout(ViewPager.LayoutParams.MATCH_PARENT, ViewGroup.LayoutParams.MATCH_PARENT);
    }


    @Override
    public void onClick(View view) {
        switch (view.getId()) {
            case R.id.tv_tell_custom:
                Intent intent = new Intent(Intent.ACTION_CALL);
                Uri data = Uri.parse("tel:" + "400-995-3636");
                intent.setData(data);
                if (ActivityCompat.checkSelfPermission(this, Manifest.permission.CALL_PHONE) != PackageManager.PERMISSION_GRANTED) {
                    // TODO: Consider calling
                    //    ActivityCompat#requestPermissions
                    // here to request the missing permissions, and then overriding
                    //   public void onRequestPermissionsResult(int requestCode, String[] permissions,
                    //                                          int[] grantResults)
                    // to handle the case where the user grants the permission. See the documentation
                    // for ActivityCompat#requestPermissions for more details.
                    return;
                }
                startActivity(intent);

                break;
            case R.id.tv_cancel:
                finish();
                break;

        }

    }
    @Override
    public boolean onTouchEvent(MotionEvent event) {
        finish();
        return true;
    }

}


